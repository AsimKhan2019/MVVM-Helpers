namespace JulMar.Windows.Interactivity.Media
{
    /// <summary>
    /// Options to control storyboard actions
    /// </summary>
    public enum ControlStoryboardOption
    {
        /// <summary>
        /// Play
        /// </summary>
        Play,
        /// <summary>
        /// Stop
        /// </summary>
        Stop,
        /// <summary>
        /// Toggle Play vs. Pause
        /// </summary>
        TogglePlayPause,
        /// <summary>
        /// Pause
        /// </summary>
        Pause,
        /// <summary>
        /// Resume
        /// </summary>
        Resume,
        /// <summary>
        /// Skip to end action
        /// </summary>
        SkipToFill
    }
}