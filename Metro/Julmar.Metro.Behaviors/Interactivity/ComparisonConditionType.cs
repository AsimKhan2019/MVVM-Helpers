namespace JulMar.Windows.Interactivity
{
    /// <summary>
    /// Comparison styles for the DataTrigger
    /// </summary>
    public enum ComparisonConditionType
    {
        /// <summary>
        /// ==
        /// </summary>
        Equal,
        /// <summary>
        /// !=
        /// </summary>
        NotEqual,
        /// <summary>
        /// Less than
        /// </summary>
        LessThan,
        /// <summary>
        /// Less than or ==
        /// </summary>
        LessThanOrEqual,
        /// <summary>
        /// Greater than
        /// </summary>
        GreaterThan,
        /// <summary>
        /// Greater than or ==
        /// </summary>
        GreaterThanOrEqual
    }
}